def build_event_analysis_prompt(
    raw_text: str,
    user_context: dict,
    criteria: list[str],
    hard_stops: list[str],
) -> str:
    """
    Строит промпт для анализа события.
    Возвращает промпт на русском языке.
    """
    criteria_list = "\n".join(f"- {c}" for c in criteria)
    hard_stops_list = "\n".join(f"- {h}" for h in hard_stops)
    attachment_type = user_context.get("attachment_type", "не определён")
    correction = user_context.get("correction_coefficient", 1.0)

    return f"""Ты — эксперт по когнитивной психологии и анализу отношений.
Твоя задача — объективно проанализировать событие в отношениях,
основываясь на принципах Системы 2 (Даниэль Канеман).

## Контекст пользователя
- Тип привязанности: {attachment_type}
- Поправочный коэффициент: {correction}
  (если коэффициент < 1.0 — пользователь склонен к негативным оценкам,
   если > 1.0 — склонен к позитивным)

## Активные критерии оценки
{criteria_list}

## Hard Stops (абсолютные фильтры)
{hard_stops_list}

## Событие для анализа
{raw_text}

## Твоя задача
1. Оцени событие по КАЖДОМУ затронутому критерию по шкале от -2 до +2:
   - +2: сильный позитивный сигнал
   - +1: слабый позитивный сигнал
   - 0: нейтрально
   - -1: слабый красный флаг
   - -2: сильный красный флаг

2. Учти тип привязанности пользователя:
   - Тревожный тип склонен преувеличивать негатив
   - Избегающий тип склонен преуменьшать значимость событий
   - Применяй поправочный коэффициент при оценке

3. Проверь наличие Hard Stop триггеров

4. Укажи когнитивное искажение если есть:
   - Эффект ореола (только позитивные/негативные события)
   - Тревожная гиперреакция
   - Непоследовательность оценок

## Формат ответа
Верни ТОЛЬКО валидный JSON без markdown блоков:
{{
    "scores": [
        {{
            "criterion_name": "название критерия из списка выше",
            "score": число от -2 до 2,
            "reasoning": "краткое обоснование на русском"
        }}
    ],
    "interpretation": "общая интерпретация события на русском",
    "bias_warning": "предупреждение о когнитивном искажении или пустая строка",
    "is_hard_stop": true или false,
    "hard_stop_name": "название Hard Stop или null"
}}"""
